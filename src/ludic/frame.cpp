#include "frame.hpp"

using namespace Ludic;

//////////////////////////////////////////////////////////////

Frame::Frame( const int& _id, const int& w, const int& h, ImageResource* _bitmap ) :
	id( _id ), width(w), height(h), bitmap( _bitmap )
{
	mask.initMask( _bitmap );
}

//////////////////////////////////////////////////////////////

Frame::~Frame()
{
	delete bitmap;
}

//////////////////////////////////////////////////////////////

Frame::operator ALLEGRO_BITMAP*() const
{
	return *bitmap;
}

//////////////////////////////////////////////////////////////

const int& Frame::getId() const
{
	return id;
}

//////////////////////////////////////////////////////////////

int Frame::getHeight() const
{
	return height;
}

//////////////////////////////////////////////////////////////

int Frame::getWidth() const
{
	return width;
}

