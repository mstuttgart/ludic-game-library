#include "tile.hpp"

using namespace Ludic;

//////////////////////////////////////////////////////////////

Tile::Tile(int& _x, int& _y, const int& _id, int& w, int& h, ImageResource* _bitmap ) :
	id(_id), position( _x, _y ), bitmap(_bitmap), box( position, w, h ) {}

//////////////////////////////////////////////////////////////

void Tile::move( const Vector2D& vec ) {
	position += vec;
}

//////////////////////////////////////////////////////////////

Tile::~Tile() {
	delete bitmap;
}

//////////////////////////////////////////////////////////////

void Tile::draw() {
	al_draw_bitmap( *bitmap, position.getX(), position.getY(), 0 );
}

//////////////////////////////////////////////////////////////

const CollisionBox& Tile::getBoundingBox() const {
	return box;
}

//////////////////////////////////////////////////////////////

Tile::operator ALLEGRO_BITMAP*() const {
	return *bitmap;
}

//////////////////////////////////////////////////////////////

int Tile::getX() const {
	return position.getX();
}

//////////////////////////////////////////////////////////////

int Tile::getY() const {
	return position.getY();
}

//////////////////////////////////////////////////////////////

const int& Tile::getId() const {
	return id;
}

